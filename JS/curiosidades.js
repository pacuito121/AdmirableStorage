let set = 1; // Variable global para saber en qué banda estamos
const totalBandas = Object.keys(Cur.Nombres).length; // Esto detecta automáticamente el total (12)

function squirrel(direccion, n) {
    let carro = document.getElementById("gallery");
    carro.innerHTML = ""; // Limpiamos el contenedor correcto
    
    // Si se pasa 'n' por el clic, saltamos a esa banda. Si no, sumamos o restamos.
    if (n) {
        set = parseInt(n);
    } else {
        (direccion) ? set++ : set--;
    }

    // Límites circulares (si baja de 1 pasa al 12, si sube de 12 pasa al 1)
    if (set < 1) set = totalBandas;
    if (set > totalBandas) set = 1;

    console.log("Banda central actual:", set);

    // Creamos los 5 elementos del carrusel (-2, -1, 0, +1, +2)
    for (let i = -2; i <= 2; i++) {
        let currentId = set + i;

        // Lógica circular para los elementos de los extremos
        if (currentId < 1) currentId += totalBandas;
        if (currentId > totalBandas) currentId -= totalBandas;

        let b = document.createElement("img");
        b.src = Cur.Image[currentId];
        
        // i == 0 es la imagen central seleccionada
        if (i === 0) {
            b.className = "Selec";
            
            // Actualizamos el nombre en pantalla
            document.getElementById("NameBand").innerHTML = Cur.Nombres[currentId];
            
            // EXTRA: Si tienes elementos HTML para las curiosidades, los llenamos aquí
            document.getElementsByClassName("datos").item(0).children.item(0).innerHTML = Cur.Data[currentId]["A"]
            document.getElementsByClassName("datos").item(1).children.item(0).innerHTML = Cur.Data[currentId]["B"]
            document.getElementsByClassName("datos").item(2).children.item(0).innerHTML = Cur.Data[currentId]["C"]
        } else {
            b.className = "Another";
            b.id = currentId; // Le damos el ID real
            
            // Al hacer clic en un elemento lateral, el carrusel salta hacia él
            b.addEventListener("click", () => {
                squirrel(true, b.id);
            });
        }
        
        carro.appendChild(b);
    }
}

