const formObj = document.querySelector("form");

// Guardar comentario
formObj.addEventListener("submit", (e) => {
    e.preventDefault();

    let discoActual = document.getElementById("sexBand").innerHTML;
    if (discoActual === "Selecciona un album") {
        return alert("Por favor, selecciona un disco de la lista primero.");
    }

    let nuevoComentario = {
        disco: discoActual,
        usuario: document.getElementById("mane").value,
        calificacion: parseInt(document.getElementById("Calif").value),
        titular: document.getElementById("Titular").value,
        texto: document.getElementById("comenta").value.trim()
    };

    let comentariosGuardados = JSON.parse(localStorage.getItem("ComentariosAdmirable")) || [];
    comentariosGuardados.push(nuevoComentario);
    localStorage.setItem("ComentariosAdmirable", JSON.stringify(comentariosGuardados));

    formObj.reset();
    document.getElementById("MSG").innerHTML = "Calificacion (5)";
    cargarComentariosYEstrellas(discoActual);
});

// Calcular promedio y mostrar lista
function cargarComentariosYEstrellas(nombreDisco) {
    let comentariosGuardados = JSON.parse(localStorage.getItem("ComentariosAdmirable")) || [];
    let comentariosDisco = comentariosGuardados.filter(c => c.disco === nombreDisco);
    
    let contenedorEstrellas = document.getElementById("stars");
    let contenedorLista = document.getElementById("lista-comentarios");

    // Si aún no has creado el id="lista-comentarios" en tu HTML, lo creamos dinámicamente
    if (!contenedorLista) {
        contenedorLista = document.createElement("article");
        contenedorLista.id = "lista-comentarios";
        document.querySelector("main").appendChild(contenedorLista);
    }

    if (comentariosDisco.length === 0) {
        contenedorEstrellas.innerHTML = "Sin calificaciones aún";
        contenedorLista.innerHTML = "<h3 align='center'>Aún no hay comentarios para este disco. ¡Sé el primero!</h3>";
        return;
    }

    // Promedio de 5 estrellas
    let suma = comentariosDisco.reduce((acc, comentario) => acc + comentario.calificacion, 0);
    let promedioBase10 = suma / comentariosDisco.length;
    let promedioBase5 = Math.round(promedioBase10 / 2);

    let estrellasHTML = "";
    for (let i = 1; i <= 5; i++) {
        estrellasHTML += i <= promedioBase5 ? "⭐" : "☆";
    }
    contenedorEstrellas.innerHTML = `${estrellasHTML} <small>(${promedioBase10.toFixed(1)}/10)</small>`;

    // Imprimir comentarios
    contenedorLista.innerHTML = "<h3 style='margin-left: 20px;'>Comentarios de la comunidad:</h3>";
    comentariosDisco.forEach(c => {
        contenedorLista.innerHTML += `
            <div style="border: 1px solid black; margin: 10px 20px; padding: 15px; background-color: #f9f9f9;">
                <h4 style="margin: 0;">${c.titular} - ${c.calificacion}/10</h4>
                <small>Por: <strong>${c.usuario}</strong></small>
                <p style="margin-top: 10px;">${c.texto}</p>
            </div>
        `;
    });
}